#include "libft.h"

t_list		*ft_lstnew(void const *content, size_t c_size)
{
	t_list	*list;

	if (!(list = (t_list *)malloc(sizeof(t_list))))
		return (NULL);
	if (content == NULL)
	{
		list->content = NULL;
		list->content_size = 0;
	}
	else
	{
		list->content = (void*)malloc(c_size);
		ft_memmove(list->content, (void *)content, c_size);
		list->content_size = c_size;
	}
	list->next = NULL;
	return (list);
}
