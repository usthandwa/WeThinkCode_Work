#include "libft.h"

int		ft_atoi(const char *s)
{
	int i;
	int res;
	int sign;

	i = 0;
	res = 0;
	sign = 1;
	while ((s[i] >= '\t' && s[i] <= '\r') || s[i] == 32 || s[i] == '+')
		i++;
	if (s[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (s[i] != '\0')
	{
		if (s[i] >= '0' && s[i] <= '9')
		{
			res = (res * 10) + (s[i] - 48);
			i++;
		}
		else
			break ;
	}
	return (res * sign);
}
